calc_lines.append('def calculate_alerts():')
calc_lines.append('    conn = get_connection()')
calc_lines.append('    c = conn.cursor()')
calc_lines.append('    c.execute(' + q34 + 'DELETE FROM alerts' + q34 + ')')
calc_lines.append('    today = datetime.now().date()')
calc_lines.append('    c.execute(' + q34 + 'SELECT sp.product_code, sp.quantity, sp.ship_date FROM shipping_plan sp' + q34 + ')')
calc_lines.append('    plans = c.fetchall()')
calc_lines.append('    for plan in plans:')
calc_lines.append('        product_code = plan[0]')
calc_lines.append('        quantity = plan[1]')
calc_lines.append('        ship_date = datetime.strptime(plan[2], ' + q39 + '%Y-%m-%d' + q39 + ').date() if plan[2] else today')
calc_lines.append('        days_remaining = (ship_date - today).days')
calc_lines.append('        c.execute(' + q34 + 'SELECT COALESCE(SUM(quantity), 0) FROM schedules WHERE work_order_no IN (SELECT order_no FROM work_orders WHERE product_code=?) AND task_status != ?' + q34 + ', (product_code, ' + q39 + 'cancelled' + q39 + '))')
calc_lines.append('        scheduled_qty = c.fetchone()[0] or 0')
calc_lines.append('        shortage = quantity - scheduled_qty')
calc_lines.append('        if days_remaining <= 3 and shortage > 0:')
calc_lines.append('            level = ' + q39 + 'red' + q39)
calc_lines.append('        elif days_remaining <= 7 and shortage > quantity * 0.5:')
calc_lines.append('            level = ' + q39 + 'yellow' + q39)
calc_lines.append('        else:')
calc_lines.append('            level = ' + q39 + 'green' + q39)
calc_lines.append('        message = product_code + ' + q39 + ' needs ' + q39 + ' + str(int(shortage)) + ' + q39 + ' more, ' + q39 + ' + str(days_remaining) + ' + q39 + ' days left' + q39)
calc_lines.append('        c.execute(' + q34 + 'INSERT INTO alerts (product_code, alert_level, due_date, quantity, scheduled_qty, shortage_qty, days_remaining, message, status) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)' + q34 + ', (product_code, level, plan[2], quantity, scheduled_qty, shortage, days_remaining, message, ' + q39 + 'active' + q39 + '))')
calc_lines.append('    conn.commit()')
calc_lines.append('    count = len(plans)')
calc_lines.append('    conn.close()')
calc_lines.append('    return count')
calc_lines.append('')
calc_lines.append('def back_calculate_semi():')
calc_lines.append('    conn = get_connection()')
calc_lines.append('    c = conn.cursor()')
calc_lines.append('    c.execute(' + q34 + 'SELECT sp.product_code, sp.quantity, sp.ship_date FROM shipping_plan sp' + q34 + ')')
