calc_lines.append('    new_orders = 0')
calc_lines.append('    for plan in plans:')
calc_lines.append('        product_code = plan[0]')
calc_lines.append('        quantity = plan[1]')
calc_lines.append('        ship_date = plan[2]')
calc_lines.append('        c.execute(' + q34 + 'SELECT child_product_code, child_product_name, quantity, process_team FROM bom WHERE parent_product_code=?' + q34 + ', (product_code,))')
calc_lines.append('        children = c.fetchall()')
calc_lines.append('        for child in children:')
calc_lines.append('            child_code = child[0]')
calc_lines.append('            child_name = child[1]')
calc_lines.append('            bom_qty = child[2] if child[2] else 1')
calc_lines.append('            required_qty = quantity * bom_qty')
calc_lines.append('            c.execute(' + q34 + 'SELECT production_days, lead_days FROM production_cycles WHERE product_code=?' + q34 + ', (child_code,))')
calc_lines.append('            cycle = c.fetchone()')
calc_lines.append('            prod_days = cycle[0] if cycle else 1')
calc_lines.append('            lead_days = cycle[1] if cycle else 0')
calc_lines.append('            ship_dt = datetime.strptime(ship_date, ' + q39 + '%Y-%m-%d' + q39 + ') if ship_date else datetime.now()')
calc_lines.append('            due_dt = ship_dt - timedelta(days=lead_days)')
calc_lines.append('            due_str = due_dt.strftime(' + q39 + '%Y-%m-%d' + q39 + ')')
calc_lines.append('            order_no = ' + q39 + 'BC-' + q39 + ' + product_code + ' + q39 + '-' + q39 + ' + child_code')
calc_lines.append('            c.execute(' + q34 + 'SELECT id FROM work_orders WHERE order_no=?' + q34 + ', (order_no,))')
calc_lines.append('            if not c.fetchone():')
calc_lines.append('                c.execute(' + q34 + 'INSERT INTO work_orders (order_no, product_code, product_name, quantity, due_date, priority, status, source) VALUES (?, ?, ?, ?, ?, ?, ?, ?)' + q34 + ', (order_no, child_code, child_name, required_qty, due_str, ' + q39 + 'P1' + q39 + ', ' + q39 + 'pending' + q39 + ', ' + q39 + '' + q39 + '))')
calc_lines.append('                new_orders += 1')
calc_lines.append('    conn.commit()')
calc_lines.append('    conn.close()')
calc_lines.append('    return new_orders')
calc_lines.append('')
